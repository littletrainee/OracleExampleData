DECLARE
    V_EXIST NUMBER;
BEGIN
    SELECT COUNT(*)
      INTO V_EXIST
      FROM USER_TABLES
     WHERE TABLE_NAME = 'PUBLISHERS';
    IF V_EXIST <> 0 THEN
        EXECUTE IMMEDIATE 'DROP TABLE PUBLISHERS CASCADE CONSTRAINT PURGE';
    END IF; 
END;
/

CREATE TABLE PUBLISHERS(
    PUBLISHER_ID CHAR(4) NOT NULL,
    PUBLISHER_NAME VARCHAR2(40),
    CITY VARCHAR2(20),
    STATE CHAR(2),
    COUNTRY VARCHAR2(30) DEFAULT('USA'),
    CONSTRAINT PK_PUBLISHERS PRIMARY KEY (PUBLISHER_ID),
    CONSTRAINT CK_PUBLISHERS_PUBLISHER_ID CHECK(PUBLISHER_ID IN ('1389', '0736', '0877', '1622', '1756') OR REGEXP_LIKE(PUBLISHER_ID, '^99[0-9]{2}$'))
);

